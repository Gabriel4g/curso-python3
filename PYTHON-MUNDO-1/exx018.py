from math import sin, cos, tan, radians
angulo = float(input(" Digite o angulo que vc deseja: "))
seno = sin(radians(angulo))
print("O angulo de {}, o SENO é {:.2f}".format(angulo, seno))
cosseno = cos(radians(angulo))
print("O angulo de {}, o COSSENO é {:.2f}".format(angulo, cosseno))
tangente = tan(radians(angulo))
print("O angulo de {}, o TANGENTE é {:.2f}".format(angulo, tangente))







